public class task2 {
    public static void main(String [] args) {
        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 10);
        System.out.println();
        for (i = 10; i != 0; i--) {
            System.out.print(i + " ");
        }
    }
}
